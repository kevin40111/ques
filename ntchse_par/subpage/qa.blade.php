<div id="q1">
				  <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q1.本問卷調查的目的是？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">由於學校評鑑僅為期一天，相當緊湊，評鑑委員需要多方蒐集意見以作成評鑑判斷，問卷調查為重要資訊來源之一，新北市政府關心所有學校成員及家長的意見，故採取網路問卷進行調查，請所有教職員、家長及學生協助提供意見。網路問卷方便、耗費成本低、且個人答案直接寫入電腦，不經人工處理，隱匿性極高。只要能夠連上網路，就能進行填答。</p>
</div>
    
<p>&nbsp;</p> 
<div id="q2">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q2.問卷調查調查對象為？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">問卷調查採普查，學校的所有老師(含代理代課老師)、家長、職員均為調查對象。在學生部份，國小五年級至高中三年級的學生均為調查對象。唯一例外的是於每學年度上學期(9月至11月)接受問卷調查的學校中，國小一年級、國中一年級(七年級)、高中一年級(十年級)之家長及學生不需接受問卷調查，原因為考量該學期貴子女剛入學，對學校狀況較不瞭解。另，跨校合聘的職員也不需進行填答。本問卷調查純屬自願，但強烈建議家長及學生透過本調查，踴躍透過表達自身意見，以協助校務精進，讓貴子女的求學環境更好。
</p>
</div>
    
<p>&nbsp;</p> 
<div id="q3">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q3.我要去那裏填網路問卷？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">您可輸入問卷網址 <a href='https://ques.cher.ntnu.edu.tw/ntchse_par' target="_blank">https://ques.cher.ntnu.edu.tw/ntchse_par</a><br />          	                                                                                 
</p>
</div>
    
<p>&nbsp;</p> 
<div id="q4">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q4.為何填問卷要輸入帳號密碼？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">為了確保您的意見能確實反映貴子女就讀學校之情況，以免您的意見被歸至其他學校。 
</p>
</div>

<p>&nbsp;</p>  
<div id="q5">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q5.問卷填答時，我的帳號密碼是什麼？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">1.請您以「新北市校務行政系統」(https://esa.ntpc.edu.tw/)的帳號密碼登入，若未曾申請帳號密碼，請詳見申請流程。</p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">2.若您忘記帳號密碼, 煩請您與貴子女的導師或學校資訊組長聯繫，請組長協助將您的密碼還原為身分證字號，再至問卷填答網址重新登入。</p>
</div>
    
   
<p>&nbsp;</p>  
<div id="q6">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q6：填問卷安全嗎？學校或教育局會不會看到我的答案？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">家長、教職員及學生填答問卷需使用新北市的單一簽入系統，以確認填答者身份，但登入之後系統即自動轉向新北市高級中等學校評鑑團隊之問卷網址，您的答案是存放在新北市高級中等學校評鑑團隊的資料庫中，教育局及學校不會知道您個人的答案。所有答案僅做為整體分析，並進行匿名處理敬請放心。</p>
</div>
  
   
<p>&nbsp;</p>  
<div id="q7">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q7.問卷調查內容是什麼？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">問卷調查內容均與貴子女就讀學校之校務運作有關，有些題目是瞭解事實狀況，有些題目則是瞭解您的個人感受或看法，每位填答者填答的題目為系統隨機指定。</p>
</div>


<p>&nbsp;</p> 
<div id="q8">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q8.問卷調查大約需要多少時間可填答完成？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">約十分鐘左右可完成問卷。</p>
</div>


  
<p>&nbsp;</p> 
<div id="q9">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q9.有些問題的答案我不確定，怎麼辦？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">關於主觀感受或看法的題目，您只要憑主觀感受回答就好，也可與其他人討論(如小孩、瞭解學校的家人、其他家長等等)後填答，您的答案無所謂對錯，只是表達您個人對貴子女就讀學校的理解、看法或感受，問卷調查結果僅作為評鑑參考資料之一，敬請放心。</p>
</div>


<p>&nbsp;</p> 
<div id="q9">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q10.填答過程中有問題怎麽辦？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">填答過程中若有問題，可以向學校反映，請學校代為轉達，或直接於上班時間與新北市高級中等學校評鑑團隊聯絡。服務專線：7734-3669；電子信箱：ntchse@deps.ntnu.edu.tw；傳真電話：33433910。</p>
</div>

  
