<div id="q1">
				  <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q1.為何要上網填問卷？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">校務評鑑只有一天，相當緊湊，評鑑委員需要多方蒐集意見以作成評鑑判斷，問卷調查是瞭解學校的重要方法，且新北市政府關心所有學校成員及家長學生的意見，所以採取網路問卷進行調查，請所有教職員、家長、學生協助提供意見。網路問卷方便、省錢、且個人答案直接寫入電腦，不經人手，隱匿性極高。只要能上網，就能完成問卷。</p>
</div>
    
<p>&nbsp;</p> 
<div id="q2">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q2.誰會被問卷調查？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">問卷調查採普查，學校的所有老師(含代理代課老師)、家長、職員都會被邀請填答問卷。在學生部份，5、6、7、8、9 年級的學生亦為調查對象。唯一例外的是上學期接受問卷調查的學校，1 年級家長、7 年級家長及學生不需接受問卷調查，其原因是剛入學，對學校狀況較不瞭解。另跨校合聘的職員也不需要填問卷。家長及學生問卷調查屬自願，但校務評鑑為學校重要活動，家長及學生請把握機會，踴躍透過問卷表達意見，以協助校務精進。
</p>
</div>
    
<p>&nbsp;</p> 
<div id="q3">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q3.我要去那裏填網路問卷？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">您可輸入問卷網址 https://ques.cher.ntnu.edu.tw/ntcse_par<br />          	                                                                                 
</p>
</div>
    
<p>&nbsp;</p> 
<div id="q4">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q4.為何填問卷要輸入帳號密碼？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">是為了確認身份，讓該填問卷的人可以登入填問卷，以免非填答對象填錯問卷，或是甲校答案跑到乙校，影響學校調查結果。不對的人上網填問卷，電腦會擋下來。另一方面，身份確認對於計算學校回收率也很重要，不然回收率會不正確。 
</p>
</div>

<p>&nbsp;</p>  
<div id="q5">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q5.問卷填答時，我的帳號密碼是什麼？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">1.帳號與密碼為貴子女在「新北市校務行政系統」(https://esa.ntpc.edu.tw/)的「自訂帳號」與密碼，預設皆為貴子女的身分證字號(第一個英文字大寫)，請先以身分證字號作為帳號密碼登入。</p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">2.若以身分證字號作為帳號密碼無法登入，請使用貴子女之自訂帳號與密碼登入。</p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">3.若貴子女忘記帳號密碼, 煩請您與貴子女的導師或學校資訊組長聯繫，請組長協助將貴子女的密碼還原為身分證字號，再至問卷填答網址重新登入。</p>
</div>
    
   
<p>&nbsp;</p>  
<div id="q6">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q6：填問卷安全嗎？學校或教育局會不會看到我的答案？ </font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">家長、教職員及學生填答問卷需使用新北市的單一簽入系統，以確認填答者身份，但登入之後系統即自動轉向師大問卷網址，您的答案是存放在師大的系統中，由師大保管，教育局及學校不會知道您個人的答案。所有答案僅做整體分析，敬請放心。</p>
</div>
  
   
<p>&nbsp;</p>  
<div id="q7">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q7.問卷調查內容是什麼？要填多久？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">問卷調查內容都是與評鑑指標有關的題目，有些是想瞭解事實狀況，有些是問您的個人感受或看法，每個人填答的題目不一樣，約十分鐘左右可完成問卷。</p>
</div>


<p>&nbsp;</p> 
<div id="q8">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q8.有些問題我不確定答案，怎麼辦？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">關於主觀感受或看法的題目，您只要憑主觀感受回答就好，也可與其他人討論(如小孩、瞭解學校的家人、其他家長等等)後填答，您的答案無所謂對錯，只是表達您個人的瞭解、看法或感受，問卷調查結果僅作為評鑑參考項目之一，敬請放心。</p>
</div>


  
<p>&nbsp;</p> 
<div id="q9">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q9.為何有人會打電話到我家來做問卷調查？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">教職員及學生一律以網路問卷進行調查，亦強烈建議家長以網路問卷填答，網路問卷便宜、便利、快速、隱匿性高。若家長無網路，請在學校所發的調查回條上勾選電話調查或紙本調查，勾選電話調查者，請留下您的電話。國立臺灣師範大學教育研究與評鑑中心會在公告的時間範圍內，打電話對您做問卷調查。請特別留意，我們不會在非調查時間與您聯絡，也不會問您跟學校辦學無關的事。您的電話號碼也不會外流作其他用途。 </p>
</div>
  
<p>&nbsp;</p> 
<div id="q10">
 <p class="MsoNormal" style="line-height:30px;margin-left:10px;margin-right:10px;"><font color="blue">Q10.有問題怎麽辦？</font></p>
 <p class="MsoNormal" style="line-height:30px;margin-left:20px;margin-right:10px;">評鑑過程有任何問題，可以向學校反映，請學校代為轉答，或與國立臺灣師範大學教育研究與評鑑中心聯絡。服務專線：77343669；電子信箱：ntcse@deps.ntnu.edu.tw；傳真電話：33433910。</p>
</div>
  
