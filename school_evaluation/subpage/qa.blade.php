            <div class="item" id="q1">
                <div class="content">
                    <a class="header">Q1.這是什麼樣的調查？</a>
                    <div class="description">
                        教育部為瞭解我國後期中等教育之辦理狀況，並配合12年國民基本教育之推動，強化「以證據為基礎」之政策規劃、提升後期中等教育辦學品質，特委託國立臺灣師範大學教育研究與評鑑中心建置「後期中等教育長期追蹤資料庫」。採抽樣調查方式，以高二及專二家長為調查對象進行網路問卷調查，調查內容包括家長對子女教育意見與作法、對學校與後期中等教育的意見、家庭基本資料與狀況等，以作為政府政策參考依據。
                    </div>
                </div>
            </div>
            <div class="item" id="q2">
                <div class="content">
                    <a class="header">Q2.調查如何進行？</a>
                    <div class="description">
                        請您根據「調查通知單」內之問卷網址上網填答。填答時間自<u style="color: red">10/4起至12/31日止</u>，任何您方便的時間均可上網填答，如果一次填不完，可以分次填答，離開前請先將一頁填完，點擊問卷下方之「下一頁」後，即能將之前填過的資料儲存，下次登入時可接續填寫。
                    </div>
                </div>
            </div>
            <div class="item" id="q3">
                <div class="content">
                    <a class="header">Q3.問卷應該由學生的哪位家長填寫？</a>
                    <div class="description">
                        家長問卷請由學生之父親、母親或監護人填寫，其他人請勿代填。
                    </div>
                </div>
            </div>
            <div class="item" id="q4">
                <div class="content">
                    <a class="header">Q4.問卷題目如何產生？</a>
                    <div class="description">
                        問卷經專家團隊透過文獻分析、政策分析、訪談、專家會議編製而成，並透過認知訪談與內外測試修正，具一定之品質。若有任何建議或問題歡迎來電詢問調查單位，也可詢問各班導師或各校承辦人。
                    </div>
                </div>
            </div>
            <div class="item" id="q5">
                <div class="content">
                    <a class="header">Q5.為何要問個人背景或家庭基本資料？</a>
                    <div class="description">
                        這些個人背景資料(如性別、職業、教育程度等)，或家庭基本資料(如收入等)是統計分析時常用控制變項(control variables)或交叉分析變項，對於分析十分重要，務請填答。填答者的個人背景資料不會被辨識，僅做整體分析，請您儘管放心。
                    </div>
                </div>
            </div>
            <div class="item" id="q6">
                <div class="content">
                    <a class="header">Q6.一定要填答這份問卷嗎？</a>
                    <div class="description">
                        後期中等教育資料庫之建置，乃為教育部執行教育基本法第九條所規定之法定職權：「教育統計、評鑑與政策研究」，懇請 貴家長協助教育部執行法定職權，讓政府能參考家長們的意見，建構更好的教育政策與教育環境，造福學生及國家未來。
                    </div>
                </div>
            </div>
            <div class="item" id="q7">
                <div class="content">
                    <a class="header">Q7.調查中填答的個人資料是否會受到保護？</a>
                    <div class="description">
                        調查單位會將調查資料匿名處理，並僅作整體分析，研究資料將根據【個人資料保護法】、相關法令規定及研究倫理處理利用。資料使用者將無法辨識填答者的身份以及答案，此外，調查單位已通過國際版資訊安全認證(ISO/IEC 27001:2013)，電腦系統及作業程序均有妥善的防護措施，請放心據實填答。
                    </div>
                </div>
            </div>
            <div class="item" id="q8">
                <div class="content">
                    <a class="header">Q8.為何要使用身分證字號登入？</a>
                    <div class="description">
                        本調查為長期追蹤(多波)調查，需以一獨特ID銜接不同波調查資料。再者，其他登入方式填答者不易永久記憶，或獨特性不足，易造成登入失敗或資料銜接錯誤，影響調查品質。所有調查程序均依【個人資料保護法】、相關法令規定及研究倫理辦理，以保護您的個人資料。
                    </div>
                </div>
            </div>
            <div class="item" id="q9">
                <div class="content">
                    <a class="header">Q9.問卷填寫大概需要多久時間？</a>
                    <div class="description">
                        因個人速度而異，平均而言約20至25分鐘左右。
                    </div>
                </div>
            </div>
            <div class="item" id="q10">
                <div class="content">
                    <a class="header">Q10.為什麼要使用網路調查？</a>
                    <div class="description">
                        網路問卷調查有下列優點：
                        <ul><li>方便(有網路隨時可以填答，填完一半離開，再回來可再接續填答，不會不見)。</li>
                            <li>環保愛地球(少砍很多樹)。</li>
                            <li>便宜(為紙本或電話調查成本十分之一)。</li>
                            <li>作答清楚(透過程式設計讓您填答容易，填寫跳答題輕鬆愉快)。</li>
                            <li>隱匿性及正確性高(資料直接寫入資料庫，不會有人看到您填的答案，也不會有他人替您輸入答案錯誤的風險)。</li>
                        </ul>
                    </div>
                </div>
            </div>
            <div class="item" id="q11">
                <div class="content">
                    <a class="header">Q11.無法用網路填寫問卷怎麼辦？</a>
                    <div class="description">
                        紙本問卷或電話訪問會大幅提高成本，也會增加個人資訊外洩的風險。請  貴家長儘量使用網路問卷填寫，也可請 貴子女教您如何於網路上填答問卷，操作步驟十分容易。若您確實無法填寫網路問卷，請您於「家長調查通知信」回條中勾選紙本或電話調查，交給各班導師。本中心將依您的意見辦理後續調查。
                    </div>
                </div>
            </div>
            <div class="item" id="q12">
                <div class="content">
                    <a class="header">Q12.「調查說明信」遺失時，該怎麼辦？</a>
                    <div class="description">
                        若您遺失調查說明信，可向各班導師或學校承辦人聯繫另行索取；另調查單位也將請各校承辦老師將電子檔公布於各校網站，您也可自行下載使用。
                    </div>
                </div>
            </div>
            <div class="item" id="q13">
                <div class="content">
                    <a class="header">Q13.為何我無法到下一頁填答？</a>
                    <div class="description">
                        若在一個頁面中，您有題目尚未填答完成，即無法前進至下一頁。此時，請檢查網頁框出紅色的題目，確認每個題目都完成填答後即可前進至下一頁。
                    </div>
                </div>
            </div>
            <div class="item" id="q14">
                <div class="content">
                    <a class="header">Q14.若我嘗試多次，都無法登入問卷網站怎麼辦？</a>
                    <div class="description">
                        若您嘗試多次，皆無法登入到問卷網站，您可逕行與本中心聯絡，請電洽(02)77343669。
                    </div>
                </div>
            </div>
            <div class="item" id="q15">
                <div class="content">
                    <a class="header">Q15.若問卷填答有錯誤，可否修改？</a>
                    <div class="description">
                        填答答案一旦按了「下一頁」後就無法再修改，按下一頁前請務必仔細檢視答案。</br>
                        若您欲修改已填答完畢之答案，請來電本中心(02)77343669。告知您孩子的學校名稱及登入帳號，以及錯填題目與答案。 但某些題目，因問卷跳答設計關係，我們無法僅刪除某一題或某一頁之答案，需刪除整份問卷之答案，您需重新填答整份問卷，敬請諒解。
                    </div>
                </div>
            </div>
            <div class="item" id="q16">
                <div class="content">
                    <a class="header">Q16.若我明明尚未填寫問卷，問卷系統卻顯示問卷已經填答完畢之訊息，應如何處理？</a>
                    <div class="description">
                        請來電本中心(02)77343669告知您的學校名稱與登入帳號，待查證後，中心會將非本人填答之資料刪除。
                    </div>
                </div>
