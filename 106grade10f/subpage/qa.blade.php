				<div class="content">
					<a class="header">Q8.一時之間填不完，必須離開怎麼辦？</a>
	 				如果一時之間填不完，只要在填完一頁後按下一頁，就會自動儲存截至目前為止已經填完的答案，當下次再登入填寫時，即可由上次離開處繼續填答，不需重新開始。
				</div>
