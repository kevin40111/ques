				<div class="content">
					<a class="header">Q9. 為何我無法到下一頁進行填答？</a>
	 				倘若在一個頁面當中，您有題目尚未填答完成，即無法前進至下一頁。此外本問卷中有部分問題需依您的事實狀況填答，因此，在您進行填答前，請先備妥下列資料，以避免因資料不全而無法順利填答之問題： （1） 個人資料：個人身份證號碼、身高、體重、會考成績。 （2） 家長或監護人基本資料：族群別、職稱、最高教育程度。
				</div>
			</div>
