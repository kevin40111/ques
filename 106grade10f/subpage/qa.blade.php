					<a class="header">Q7.這項調查的填答時間與問卷內容？</a>
	 				本問卷填答時間約為25分鐘，題目均為後期中等教育中重要的議題，分成以下七個部份： 第一部份：資料確認、第二部份：國中學習狀況、第三部份：國中升學狀況、第四部份：當初選擇高中職五專的考量、第五部份：課程方案評估：數學科、第六部份：課程方案評估：英文科、第七部份：家庭狀況、第八部份：個人情況與規劃、第九部份：開放式意見
				</div>
			</div>
