					<a class="header">Q10.我所填寫的答案，學校會不會看到呢？</a>
	 				您的填答資料，將直接寫入本中心資料庫，您的填答資料都將去除可辨識之個人身分資料，保密處理，再供學校及政府分析使用，請您放心填答。
