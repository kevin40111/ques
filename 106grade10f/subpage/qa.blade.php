					<a class="header">Q6.為何要使用身分證字號或居留證字號登入？</a>
	 				本調查為長期追蹤(多波)調查，需以一獨特ID銜接不同波調查資料。再者，其他登入方式填答者不易永久記憶，或獨特性不足，易造成資料寫入失敗問題。所有調查程序均依【個人資料保護法】、【教育部資料庫建置說明】之規定，並遵守研究倫理，以保護您的個人資料。
